#include "libft.h"

void	ft_putnbr(int n)
{
	int sign;

	sign = 1;
	if (n < 0)
	{
		ft_putchar('-');
		sign = -1;
	}
	if (n / 10 != 0)
		ft_putnbr(n / 10 * sign);
	ft_putchar((n % 10) * sign + '0');
}
